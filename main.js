mon.onmousemove = function () {
    var popup = this.getElementsByTagName('p')[0];
    popup.style.visibility = 'visible';
    var bar = this.getElementsByClassName('bar-chart')[0];
    bar.style.backgroundColor = 'hsl(186, 34%, 60%)';
}

mon.onmouseleave = function () {
    var popup = this.getElementsByTagName('p')[0];
    popup.style.visibility = 'hidden';
    var bar = this.getElementsByClassName('bar-chart')[0];
    bar.style.backgroundColor = 'hsl(10, 79%, 65%)';
}

